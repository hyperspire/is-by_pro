#[actix_web::main]
async fn main() -> std::io::Result<()> {
  rustls::crypto::aws_lc_rs::default_provider()
    .install_default()
    .expect("Failed to install rustls crypto provider");

  let _ = dotenvy::from_filename(".env");
  let _ = dotenvy::from_filename(".env/GITHUB_CLIENT_SECRET.env");
  let _ = dotenvy::from_filename(".env/MYSQL_PASSWORD.env");

  let db_pool = create_db_pool()
    .await
    .expect("Failed to connect to MySQL");

  let app_state = AppState {
    db_pool,
    github_client_id: std::env::var("GITHUB_CLIENT_ID")
      .expect("Missing GITHUB_CLIENT_ID in environment file or shell"),
    github_client_secret: std::env::var("GITHUB_CLIENT_SECRET")
      .expect("Missing GITHUB_CLIENT_SECRET in environment file or shell"),
    github_redirect_uri: std::env::var("GITHUB_REDIRECT_URI")
      .expect("Missing GITHUB_REDIRECT_URI in environment file or shell"),
  };

  let http_server = HttpServer::new(|| {
    App::new().default_service(web::to(redirect_to_https))
  })
  .bind(("0.0.0.0", 80))?
  .run();

  let https_server = HttpServer::new(move || {
    let allowed_origin = format!("https://{}", DOMAIN);
    let cors = Cors::default()
      .allowed_origin(&allowed_origin)
      .allowed_methods(vec!["GET", "POST"])
      .allow_any_header();

    App::new()
      .wrap(cors)
      .wrap_fn(move |req, srv| {
        let method = req.method().clone();
        let host = req
          .connection_info()
          .host()
          .split(':')
          .next()
          .unwrap_or_default()
          .to_owned();
        let host_ok = host.eq_ignore_ascii_case(DOMAIN);

        let origin_ok = req
          .headers()
          .get("origin")
          .and_then(|v| v.to_str().ok())
          .map(|origin| origin.eq_ignore_ascii_case(&allowed_origin))
          .unwrap_or(true);

        let method_ok = method == Method::GET || method == Method::POST;

        type MiddlewareFuture = std::pin::Pin<
          Box<
            dyn std::future::Future<
              Output = Result<
                actix_web::dev::ServiceResponse<
                  actix_web::body::EitherBody<actix_web::body::BoxBody>
                >,
                actix_web::Error,
              >,
            >,
          >,
        >;

        if !host_ok || !origin_ok {
          let response = req.into_response(HttpResponse::Forbidden().body("Forbidden"));
          let fut: MiddlewareFuture = Box::pin(async move {
            Ok(response.map_into_right_body::<actix_web::body::BoxBody>())
          });
          return fut;
        }

        if !method_ok {
          let response = req.into_response(HttpResponse::MethodNotAllowed().body("Method Not Allowed"));
          let fut: MiddlewareFuture = Box::pin(async move {
            Ok(response.map_into_right_body::<actix_web::body::BoxBody>())
          });
          return fut;
        }

        let fut = srv.call(req);
        let fut: MiddlewareFuture = Box::pin(async move {
          let res = fut.await?;
          let res = res.map_into_boxed_body();
          Ok(res.map_into_left_body::<actix_web::body::BoxBody>())
        });
        fut
      })
      .app_data(web::Data::new(app_state.clone()))
      .service(hello)
      .service(create_post)
      .service(create_reply)
      .service(show_post)
      .service(show_post_get)
      .service(edit_profile)
      .service(update_profile)
      .service(delete_post)
      .service(edit_post)
      .service(update_post)
      .service(view_profile)
      .service(github_auth_start)
      .service(github_auth_callback)
      .service(Files::new("/", "./webroot"))
  })
  .bind_rustls_0_23(("0.0.0.0", 443), load_rustls_config())?
  .run();

  tokio::try_join!(http_server, https_server)?;
  Ok(())
}