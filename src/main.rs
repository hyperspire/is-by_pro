#[post("/v1/ackpost")]
async fn acknowledge_post(
  state: web::Data<AppState>,
  payload: web::Form<AckPostRequest>,
  req: HttpRequest,
) -> impl Responder {
  let session_uid = match get_session_uid(&req) {
    Some(uid) => uid,
    None => return HttpResponse::Unauthorized().body("Login required"),
  };

  if session_uid != payload.ib_uid {
    return HttpResponse::Forbidden().body("Session mismatch");
  }

  let insert_result = sqlx::query(
    "INSERT IGNORE INTO post_ack (postid, ib_uid) VALUES (?, ?)",
  )
  .bind(&payload.pid)
  .bind(session_uid)
  .execute(&state.db_pool)
  .await;

  let insert_result = match insert_result {
    Ok(result) => result,
    Err(err) => {
      return HttpResponse::InternalServerError().body(format!("Failed to acknowledge post: {}", err));
    }
  };

  if insert_result.rows_affected() > 0 {
    let update_result = sqlx::query(
      "UPDATE post SET acknowledged_count = COALESCE(acknowledged_count, 0) + 1 WHERE postid = ? LIMIT 1",
    )
    .bind(&payload.pid)
    .execute(&state.db_pool)
    .await;

    if let Err(err) = update_result {
      return HttpResponse::InternalServerError().body(format!("Failed to acknowledge post: {}", err));
    }
  }

  let fallback_location = format!(
    "/v1/showpost?ib_uid={}&ib_user={}&pid={}",
    payload.ib_uid,
    payload.ib_user,
    payload.pid
  );

  let location = req
    .headers()
    .get("referer")
    .and_then(|value| value.to_str().ok())
    .filter(|value| !value.trim().is_empty())
    .unwrap_or(&fallback_location)
    .to_string();

  HttpResponse::SeeOther()
    .insert_header(("Location", location))
    .finish()
}

#[get("/v1/profile/{ib_user}")]
async fn view_profile(
  req: HttpRequest,
  path: web::Path<String>,
  state: web::Data<AppState>,
) -> impl Responder {
  let ib_user = path.into_inner();

  if let Some(stripped) = ib_user.strip_prefix('@') {
    return HttpResponse::PermanentRedirect()
      .insert_header(("Location", format!("/v1/profile/{}", stripped)))
      .finish();
  }

  let normalized = ib_user.clone();

  // 1) Accept numeric uid directly in path.
  let mut resolved_uid = normalized.parse::<i64>().ok();

  // 2) Resolve from profile github handle.
  if resolved_uid.is_none() {
    let lookup = sqlx::query_as::<_, ProfileLookupRow>(
      "SELECT ib_uid FROM pro WHERE LOWER(github) = LOWER(?) LIMIT 1",
    )
    .bind(&normalized)
    .fetch_optional(&state.db_pool)
    .await;

    match lookup {
      Ok(Some(row)) => {
        if let Ok(parsed) = row.ib_uid.parse::<i64>() {
          resolved_uid = Some(parsed);
        }
      }
      Ok(None) => {}
      Err(err) => {
        return HttpResponse::InternalServerError().body(format!("Profile lookup failed: {}", err));
      }
    }
  }

  // CREATE TABLE user (ib_uid varchar(64) PRIMARY KEY, username varchar(255));
  // 3) Fallback by username in legacy user table.
  if resolved_uid.is_none() {
    let user_lookup = sqlx::query_as::<_, UsernameLookupRow>(
      "SELECT ib_uid FROM user WHERE LOWER(username) = LOWER(?) LIMIT 1",
    )
    .bind(&normalized)
    .fetch_optional(&state.db_pool)
    .await;

    match user_lookup {
      Ok(Some(row)) => {
        if let Ok(parsed) = row.ib_uid.parse::<i64>() {
          resolved_uid = Some(parsed);
        }
      }
      Ok(None) => {}
      Err(err) => {
        return HttpResponse::InternalServerError().body(format!("Username lookup failed: {}", err));
      }
    }
  }

  let ib_uid = match resolved_uid {
    Some(uid) => uid,
    None => {
      return HttpResponse::NotFound()
        .content_type("text/html; charset=utf-8")
        .body(format!(
          "<!DOCTYPE html><html lang=\"en-US\"><head><meta charset=\"UTF-8\"><title>Profile Not Found</title></head><body><p>Profile not found: {}</p></body></html>",
          escape_html(&ib_user)
        ));
    }
  };

  match render_profile_html(&state, ib_uid, &ib_user, get_session_uid(&req)).await {
    Ok(html) => HttpResponse::Ok()
      .content_type("text/html; charset=utf-8")
      .body(html),
    Err(err) => HttpResponse::InternalServerError().body(err),
  }
}

