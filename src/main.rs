fn load_rustls_config() -> ServerConfig {
  let cert_file = &mut BufReader::new(File::open(IB_CA_CERT).unwrap());
  let key_file  = &mut BufReader::new(File::open(IB_CA_KEY).unwrap());

  let cert_chain: Vec<CertificateDer> = certs(cert_file).map(|c| c.unwrap()).collect();
  let key = private_key(key_file).unwrap().unwrap();

  ServerConfig::builder()
    .with_no_client_auth()
    .with_single_cert(cert_chain, key)
    .unwrap()
}

fn escape_html(input: &str) -> String {
  input
    .replace('&', "&amp;")
    .replace('<', "&lt;")
    .replace('>', "&gt;")
    .replace('"', "&quot;")
    .replace('\'', "&#39;")
}

fn escape_mysql_regex_token(input: &str) -> String {
  let mut escaped = String::with_capacity(input.len());

  for ch in input.chars() {
    match ch {
      '\\' | '.' | '^' | '$' | '*' | '+' | '?' | '(' | ')' | '[' | ']' | '{' | '}' | '|' => {
        escaped.push('\\');
        escaped.push(ch);
      }
      _ => escaped.push(ch),
    }
  }

  escaped
}

fn url_encode_component(input: &str) -> String {
  let mut encoded = String::with_capacity(input.len());

  for byte in input.bytes() {
    if byte.is_ascii_alphanumeric() || matches!(byte, b'-' | b'_' | b'.' | b'~') {
      encoded.push(byte as char);
    } else {
      encoded.push_str(&format!("%{:02X}", byte));
    }
  }

  encoded
}

fn normalize_hashtag(raw_tag: &str) -> Option<String> {
  let mut normalized = raw_tag.trim();

  if let Some(without_hash) = normalized.strip_prefix('#') {
    normalized = without_hash;
  }

  if normalized.is_empty() {
    return None;
  }

  if !normalized
    .chars()
    .all(|ch| ch.is_ascii_alphanumeric() || ch == '_')
  {
    return None;
  }

  Some(normalized.to_lowercase())
}

fn trim_trailing_url_punctuation(raw_url: &str) -> (&str, &str) {
  let trimmed = raw_url.trim_end_matches(|ch: char| {
    matches!(ch, '.' | ',' | '!' | '?' | ':' | ';' | ')' | ']')
  });

  let suffix = &raw_url[trimmed.len()..];
  (trimmed, suffix)
}

fn render_post_with_hashtags(raw_text: &str, ib_uid: i64, ib_user: &str) -> String {
  let mut rendered = String::new();
  let chars: Vec<(usize, char)> = raw_text.char_indices().collect();
  let mut cursor = 0usize;
  let mut index = 0usize;

  while index < chars.len() {
    let (byte_pos, ch) = chars[index];
    let previous_is_token_char = index > 0
      && (chars[index - 1].1.is_ascii_alphanumeric() || chars[index - 1].1 == '_');
    let scheme_url_start = raw_text[byte_pos..].starts_with("http://") || raw_text[byte_pos..].starts_with("https://");
    let www_url_start = !previous_is_token_char && raw_text[byte_pos..].starts_with("www.");

    if scheme_url_start || www_url_start {
      let mut url_end_index = index;

      while url_end_index < chars.len() && !chars[url_end_index].1.is_whitespace() {
        url_end_index += 1;
      }

      rendered.push_str(&escape_html(&raw_text[cursor..byte_pos]));

      let url_end_byte = if url_end_index < chars.len() {
        chars[url_end_index].0
      } else {
        raw_text.len()
      };

      let raw_url = &raw_text[byte_pos..url_end_byte];
      let (trimmed_url, trailing_punctuation) = trim_trailing_url_punctuation(raw_url);
      let href = if www_url_start {
        format!("https://{}", trimmed_url)
      } else {
        trimmed_url.to_string()
      };

      rendered.push_str(&format!(
        r#"<a class="post-link" href="{href}" target="_blank" rel="noopener">{label}</a>"#,
        href = escape_html(&href),
        label = escape_html(trimmed_url)
      ));
      rendered.push_str(&escape_html(trailing_punctuation));

      cursor = url_end_byte;
      index = url_end_index;
      continue;
    }

    if (ch == '#' || ch == '@') && !previous_is_token_char {
      let mut token_end_index = index + 1;

      while token_end_index < chars.len() {
        let next_char = chars[token_end_index].1;
        if next_char.is_ascii_alphanumeric() || next_char == '_' {
          token_end_index += 1;
        } else {
          break;
        }
      }

      if token_end_index > index + 1 {
        rendered.push_str(&escape_html(&raw_text[cursor..byte_pos]));

        let token_start_byte = byte_pos + ch.len_utf8();
        let token_end_byte = if token_end_index < chars.len() {
          chars[token_end_index].0
        } else {
          raw_text.len()
        };

        let token_value = &raw_text[token_start_byte..token_end_byte];
        let href = if ch == '#' {
          format!(
            "https://{domain}/v1/searchposts?ib_uid={ib_uid}&ib_user={ib_user}&tag=%23{tag}",
            domain = DOMAIN,
            ib_uid = ib_uid,
            ib_user = url_encode_component(ib_user),
            tag = url_encode_component(token_value)
          )
        } else {
          format!(
            "https://{domain}/v1/profile/{username}",
            domain = DOMAIN,
            username = url_encode_component(token_value)
          )
        };

        let class_name = if ch == '#' { "post-tag" } else { "post-mention" };
        rendered.push_str(&format!(
          r#"<a class="{class_name}" href="{href}">{prefix}{token}</a>"#,
          class_name = class_name,
          href = href,
          prefix = ch,
          token = escape_html(token_value)
        ));

        cursor = token_end_byte;
        index = token_end_index;
        continue;
      }
    }

    index += 1;
  }

  if cursor < raw_text.len() {
    rendered.push_str(&escape_html(&raw_text[cursor..]));
  }

  rendered
}

fn extract_hashtags(raw_text: &str) -> Vec<String> {
  let chars: Vec<(usize, char)> = raw_text.char_indices().collect();
  let mut index = 0usize;
  let mut tags = Vec::new();
  let mut seen = HashSet::new();

  while index < chars.len() {
    let (byte_pos, ch) = chars[index];
    let previous_is_tag_char = index > 0
      && (chars[index - 1].1.is_ascii_alphanumeric() || chars[index - 1].1 == '_');

    if ch == '#' && !previous_is_tag_char {
      let mut tag_end_index = index + 1;

      while tag_end_index < chars.len() {
        let next_char = chars[tag_end_index].1;
        if next_char.is_ascii_alphanumeric() || next_char == '_' {
          tag_end_index += 1;
        } else {
          break;
        }
      }

      if tag_end_index > index + 1 {
        let tag_start_byte = byte_pos + '#'.len_utf8();
        let tag_end_byte = if tag_end_index < chars.len() {
          chars[tag_end_index].0
        } else {
          raw_text.len()
        };
        let tag_value = &raw_text[tag_start_byte..tag_end_byte];

        if let Some(normalized) = normalize_hashtag(tag_value) {
          if seen.insert(normalized.clone()) {
            tags.push(normalized);
          }
        }

        index = tag_end_index;
        continue;
      }
    }

    index += 1;
  }

  tags
}

fn extract_mentions(raw_text: &str) -> Vec<String> {
  let chars: Vec<(usize, char)> = raw_text.char_indices().collect();
  let mut index = 0usize;
  let mut mentions = Vec::new();
  let mut seen = HashSet::new();

  while index < chars.len() {
    let (byte_pos, ch) = chars[index];
    let previous_is_token_char = index > 0
      && (chars[index - 1].1.is_ascii_alphanumeric() || chars[index - 1].1 == '_');

    if ch == '@' && !previous_is_token_char {
      let mut mention_end_index = index + 1;

      while mention_end_index < chars.len() {
        let next_char = chars[mention_end_index].1;
        if next_char.is_ascii_alphanumeric() || next_char == '_' {
          mention_end_index += 1;
        } else {
          break;
        }
      }

      if mention_end_index > index + 1 {
        let mention_start_byte = byte_pos + '@'.len_utf8();
        let mention_end_byte = if mention_end_index < chars.len() {
          chars[mention_end_index].0
        } else {
          raw_text.len()
        };
        let mention_value = &raw_text[mention_start_byte..mention_end_byte];

        if seen.insert(mention_value.to_lowercase()) {
          mentions.push(mention_value.to_string());
        }

        index = mention_end_index;
        continue;
      }
    }

    index += 1;
  }

  mentions
}

fn highlight_terms(raw_text: &str, terms: &[String]) -> String {
  if terms.is_empty() {
    return escape_html(raw_text);
  }

  let chars: Vec<char> = raw_text.chars().collect();
  let lower_chars: Vec<char> = raw_text.to_lowercase().chars().collect();
  let n = chars.len();
  let mut matched = vec![false; n];

  for term in terms {
    if term.is_empty() {
      continue;
    }
    let term_chars: Vec<char> = term.to_lowercase().chars().collect();
    let tlen = term_chars.len();
    let mut i = 0;
    while i + tlen <= n {
      if lower_chars[i..i + tlen] == term_chars[..] {
        for j in i..i + tlen {
          matched[j] = true;
        }
        i += tlen;
      } else {
        i += 1;
      }
    }
  }

  let mut result = String::new();
  let mut in_match = false;
  let mut buffer = String::new();

  for (i, &ch) in chars.iter().enumerate() {
    if matched[i] && !in_match {
      result.push_str(&escape_html(&buffer));
      buffer.clear();
      in_match = true;
      buffer.push(ch);
    } else if !matched[i] && in_match {
      result.push_str("<strong>");
      result.push_str(&escape_html(&buffer));
      result.push_str("</strong>");
      buffer.clear();
      in_match = false;
      buffer.push(ch);
    } else {
      buffer.push(ch);
    }
  }

  if !buffer.is_empty() {
    if in_match {
      result.push_str("<strong>");
      result.push_str(&escape_html(&buffer));
      result.push_str("</strong>");
    } else {
      result.push_str(&escape_html(&buffer));
    }
  }

  result
}

async fn render_related_userlist_html(
  state: &AppState,
  source_uid: i64,
  source_ibp: &str,
) -> String {
  let interests: Vec<String> = source_ibp
    .split(',')
    .map(|term| term.trim().to_lowercase())
    .filter(|term| !term.is_empty())
    .collect();

  if interests.is_empty() {
    return "<br><p><em>:[[ :no-related-users: ]]:</em></p>".to_string();
  }

  let regex_terms: Vec<String> = interests
    .iter()
    .map(|term| escape_mysql_regex_token(term))
    .collect();
  let pattern = format!(
    "(^|[[:space:],])({})([[:space:],]|$)",
    regex_terms.join("|")
  );

  let related_rows = sqlx::query_as::<_, RelatedUserRow>(
      "SELECT CAST(candidate.ib_uid AS CHAR CHARACTER SET utf8mb4) AS ib_uid FROM pro AS candidate WHERE candidate.ib_uid <> ? AND LOWER(COALESCE(candidate.ibp, '')) REGEXP ? ORDER BY RAND() LIMIT 5"
    )
    .bind(source_uid)
    .bind(&pattern)
    .fetch_all(&state.db_pool)
    .await;

  let related_rows = match related_rows {
    Ok(rows) => rows,
    Err(_) => return "<p><em>:[[ :related-user-lookup-failed: ]]:</em></p>".to_string(),
  };

  let mut related_html = String::new();

  for row in related_rows {
    let username_row = sqlx::query_as::<_, RelatedUsernameRow>(
        "SELECT username FROM user WHERE CONVERT(ib_uid USING utf8mb4) COLLATE utf8mb4_unicode_ci = ? LIMIT 1"
      )
      .bind(&row.ib_uid)
      .fetch_optional(&state.db_pool)
      .await;

    let username = match username_row {
      Ok(Some(row)) if !row.username.trim().is_empty() => row.username,
      _ => continue,
    };

    related_html += &format!(
      r#"<br><p><a href="https://{domain}/v1/profile/{username}">{username}</a></p>"#,
      domain = DOMAIN,
      username = escape_html(&username)
    );
  }

  if related_html.is_empty() {
    "<br><p><em>:[[ :no-related-users: ]]:</em></p>".to_string()
  } else {
    related_html
  }
}

async fn lookup_ibp_by_uid(state: &AppState, uid: i64) -> Option<String> {
  let row = sqlx::query_as::<_, ProRow>(
      "SELECT ibp, pro, location, services, website, github FROM pro WHERE ib_uid = ? LIMIT 1"
    )
    .bind(uid)
    .fetch_optional(&state.db_pool)
    .await
    .ok()
    .flatten()?;

  Some(row.ibp)
}

