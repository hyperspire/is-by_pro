fn load_rustls_config() -> ServerConfig {
  let cert_file = &mut BufReader::new(File::open(IB_CA_CERT).unwrap());
  let key_file  = &mut BufReader::new(File::open(IB_CA_KEY).unwrap());

  let cert_chain: Vec<CertificateDer> = certs(cert_file).map(|c| c.unwrap()).collect();
  let key = private_key(key_file).unwrap().unwrap();

  ServerConfig::builder()
    .with_no_client_auth()
    .with_single_cert(cert_chain, key)
    .unwrap()
}

fn escape_html(input: &str) -> String {
  input
    .replace('&', "&amp;")
    .replace('<', "&lt;")
    .replace('>', "&gt;")
    .replace('"', "&quot;")
    .replace('\'', "&#39;")
}

fn escape_mysql_regex_token(input: &str) -> String {
  let mut escaped = String::with_capacity(input.len());

  for ch in input.chars() {
    match ch {
      '\\' | '.' | '^' | '$' | '*' | '+' | '?' | '(' | ')' | '[' | ']' | '{' | '}' | '|' => {
        escaped.push('\\');
        escaped.push(ch);
      }
      _ => escaped.push(ch),
    }
  }

  escaped
}

async fn render_related_userlist_html(
  state: &AppState,
  current_uid: i64,
  current_pro: &str,
) -> String {
  let interests: Vec<String> = current_pro
    .split(',')
    .map(|term| term.trim().to_lowercase())
    .filter(|term| !term.is_empty())
    .collect();

  if interests.is_empty() {
    return "<p><em>:[[ :no-related-users: ]]:</em></p>".to_string();
  }

  let regex_terms: Vec<String> = interests
    .iter()
    .map(|term| escape_mysql_regex_token(term))
    .collect();
  let pattern = format!(
    "(^|[[:space:],])({})([[:space:],]|$)",
    regex_terms.join("|")
  );

  let related_rows = sqlx::query_as::<_, RelatedUserRow>(
      "SELECT u.username FROM isby.pro AS p LEFT JOIN isby.user AS u ON CONVERT(u.ib_uid USING utf8mb4) COLLATE utf8mb4_unicode_ci = CAST(p.ib_uid AS CHAR CHARACTER SET utf8mb4) COLLATE utf8mb4_unicode_ci WHERE p.ib_uid <> ? AND LOWER(COALESCE(p.pro, '')) REGEXP ? ORDER BY p.ib_uid DESC LIMIT 5"
    )
    .bind(current_uid)
    .bind(&pattern)
    .fetch_all(&state.db_pool)
    .await;

  let related_rows = match related_rows {
    Ok(rows) => rows,
    Err(_) => return "<p><em>:[[ :related-user-lookup-failed: ]]:</em></p>".to_string(),
  };

  let mut related_html = String::new();

  for row in related_rows {
    let username = match row.username {
      Some(username) if !username.trim().is_empty() => username,
      _ => continue,
    };

    related_html += &format!(
      r#"<p><a href="https://{domain}/v1/profile/{username}">{username}</a></p>"#,
      domain = DOMAIN,
      username = escape_html(&username)
    );
  }

  if related_html.is_empty() {
    "<p><em>:[[ :no-related-users: ]]:</em></p>".to_string()
  } else {
    related_html
  }
}

fn render_post_meta(ib_uid: &str, username: &str, timestamp: &str) -> String {
  let profile_target = if username.trim().is_empty() {
    ib_uid
  } else {
    username
  };

  format!(
    r#"<div class="post-meta"><a class="post-author" href="https://{domain}/v1/profile/{profile_target}">{username}</a><span class="post-timestamp">{timestamp}</span></div>"#,
    domain = DOMAIN,
    profile_target = escape_html(profile_target),
    username = escape_html(username),
    timestamp = escape_html(timestamp)
  )
}

fn get_session_uid(req: &HttpRequest) -> Option<i64> {
  req.cookie("ib_uid")
    .and_then(|cookie| cookie.value().parse::<i64>().ok())
}

async fn redirect_to_https(req: HttpRequest) -> HttpResponse {
  let host = req.connection_info().host().to_owned();
  let path = req.uri().path_and_query().map(|p| p.as_str()).unwrap_or("/");
  HttpResponse::MovedPermanently()
    .insert_header(("Location", format!("https://{}{}", host, path)))
    .finish()
}

async fn create_db_pool() -> Result<MySqlPool, sqlx::Error> {
  let mysql_password = std::env::var("MYSQL_PASSWORD")
    .expect("Missing MYSQL_PASSWORD in environment file or shell");

  MySqlPoolOptions::new()
    .max_connections(5)
    .connect_with(
      sqlx::mysql::MySqlConnectOptions::new()
        .host("localhost")
        .username(MYSQL_USER)
        .password(&mysql_password)
        .database(MYSQL_DATABASE),
    )
    .await
}

async fn ensure_legacy_user_from_github(
  state: &AppState,
  github_id: u64,
  github_username: &str,
) -> Result<(), sqlx::Error> {
  // CREATE TABLE isby.user (ib_uid varchar(64) PRIMARY KEY, username varchar(255));
  sqlx::query("INSERT IGNORE INTO isby.user (ib_uid, username) VALUES (?, ?)")
    .bind(github_id.to_string())
    .bind(github_username)
    .execute(&state.db_pool)
    .await?;

  Ok(())
}

