#[post("/v1/post")]
async fn create_post(
  state: web::Data<AppState>,
  payload: web::Json<NewPostRequest>,
) -> impl Responder {
  const MAX_POST_LEN: usize = 1024;

  if payload.post.trim().is_empty() {
    return HttpResponse::BadRequest().json(PostResponse {
      success: false,
      message: "Post cannot be empty".to_string(),
      postid: None,
    });
  }

  if payload.post.chars().count() > MAX_POST_LEN {
    return HttpResponse::BadRequest().json(PostResponse {
      success: false,
      message: format!("Post cannot exceed {} characters", MAX_POST_LEN),
      postid: None,
    });
  }

  let postid = Uuid::new_v4().to_string();

  // CREATE TABLE isby.post (ib_uid varchar(64), postid varchar(64), parentid varchar(64), post varchar(1024), timestamp varchar(32));
  // ALTER TABLE isby.post MODIFY COLUMN parentid VARCHAR(64) NOT NULL DEFAULT '';
  let result = sqlx::query(
    "INSERT INTO isby.post (ib_uid, postid, post, `timestamp`) VALUES (?, ?, ?, NOW())",
  )
  .bind(payload.ib_uid)
  .bind(&postid)
  .bind(&payload.post)
  .execute(&state.db_pool)
  .await;

  match result {
    Ok(_) => HttpResponse::Ok()
      .insert_header(("ib_user", payload.ib_user.clone()))
      .json(PostResponse {
        success: true,
        message: "Post created".to_string(),
        postid: Some(postid),
      }),
    Err(err) => HttpResponse::InternalServerError().json(PostResponse {
      success: false,
      message: format!("Failed to create post: {}", err),
      postid: None,
    }),
  }
}

