fn get_rank_info(acks: i64) -> &'static RankInfo {
  for rank in RANK_TABLE {
    if acks >= rank.threshold {
      return rank;
    }
  }
  &RANK_TABLE[RANK_TABLE.len() - 1]
}

fn get_rank_asset(acks: i64) -> &'static str {
  get_rank_info(acks).asset
}

